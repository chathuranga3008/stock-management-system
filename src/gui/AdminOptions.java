    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String dNow = sdf1.format(new Date());

        try {
            String filePath = "/reports/stock_drop.jrxml";

            InputStream is = getClass().getResourceAsStream(filePath);

            JasperReport c_jasp = (JasperReport) JasperCompileManager.compileReport(is);

            HashMap parameters = new HashMap();
            parameters.put("report_date", dNow);

            Connection dataSource = MySQL.getConnection();

            JasperPrint jp = JasperFillManager.fillReport(c_jasp, parameters, dataSource);

            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(AdminOptions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

