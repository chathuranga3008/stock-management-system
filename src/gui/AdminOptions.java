    private void DailyEarnings() {
        try {
            double total = 0;

            SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
            String dNow = sdf1.format(new Date());

            ResultSet rs = MySQL.search("SELECT * FROM invoice_payment INNER JOIN invoice ON invoice_payment.invoice_id = invoice.id WHERE invoice.date_time LIKE '" + dNow + "%'");
            while (rs.next()) {
                String t = rs.getString("payment");
                total = total + Double.parseDouble(t);
            }
            jLabel2.setText("Rs. " + df.format(total));
        } catch (Exception ex) {
        }
    }

