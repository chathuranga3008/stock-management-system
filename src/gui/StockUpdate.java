    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String paymentType = jComboBox1.getSelectedItem().toString();
        String payment = jTextField3.getText();

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(m, "Please add products", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (paymentType.equals("Select")) {
            JOptionPane.showMessageDialog(m, "Please select payment method", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[0]|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(m, "Invalid payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            long mTime = System.currentTimeMillis();

            String uniqueId = mTime + "-" + Main.userId;

            String sid = jLabel2.getText();

            SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dNow = sdf1.format(new Date());

            MySQL.iud("INSERT INTO `stock_update`(`source_id`, `date_time`, `user_id`, `unique_id`) VALUES ('" + sid + "', '" + dNow + "', '" + Main.userId + "', '" + uniqueId + "')");

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `stock_update` WHERE `unique_id`='" + uniqueId + "'");
                rs.next();
                String id = rs.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `payment_type` WHERE `name` = '" + paymentType + "'");
                rs2.next();
                String paymentTypeId = rs2.getString("id");

                String balance = jLabel30.getText();

                MySQL.iud("INSERT INTO `stock_update_payment`(`stock_update_id`, `payment_type_id`, `payment`, `balance`) VALUES ('" + id + "', '" + paymentTypeId + "', '" + payment + "', '" + balance + "')");

                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    String pid = jTable1.getValueAt(i, 1).toString();
                    String qty = jTable1.getValueAt(i, 7).toString();
                    String sellingprice = jTable1.getValueAt(i, 9).toString();
                    String buyingprice = jTable1.getValueAt(i, 8).toString();

                    ResultSet rs3 = MySQL.search("SELECT * FROM `stock` WHERE `product_id` = '" + pid + "' AND `selling_price` = '" + sellingprice + "'");
                    {
                        String stock_id;
                        if (rs3.next()) {
                            stock_id = rs3.getString("id");
                            String stock_qty = rs3.getString("quantity");

                            int updatedQty = Integer.parseInt(stock_qty) + Integer.parseInt(qty);

                            MySQL.iud("UPDATE `stock` SET `quantity` = '" + updatedQty + "' WHERE `id` = '" + stock_id + "'");
                        } else {
                            MySQL.iud("INSERT INTO `stock`(`product_id`, `quantity`, `selling_price`) VALUES('" + pid + "', '" + qty + "', '" + sellingprice + "')");

                            ResultSet rs4 = MySQL.search("SELECT * FROM `stock` WHERE `product_id` = '" + pid + "' AND `selling_price` = '" + sellingprice + "'");
                            rs4.next();
                            stock_id = rs4.getString("id");
                        }
                        MySQL.iud("INSERT INTO `stock_update_item`(`quantity`, `buying_price`, `stock_update_id`, `stock_id`) VALUES('" + qty + "', '" + buyingprice + "', '" + id + "', '" + stock_id + "')");
                    }
                }

                {
                    String filePath = "/reports/stock_update.jrxml";

                    InputStream is = getClass().getResourceAsStream(filePath);

                    JasperReport c_jasp = (JasperReport) JasperCompileManager.compileReport(is);

                    HashMap parameters = new HashMap();
                    parameters.put("supplier_name", jLabel4.getText());
                    parameters.put("supplier_address", jLabel6.getText());
                    parameters.put("supplier_contact", jLabel8.getText());
                    parameters.put("stock_update_id", id);
                    parameters.put("stock_update_date", dNow);
                    parameters.put("payment_mode", paymentType);
                    parameters.put("user_name", Main.userName);
                    parameters.put("total_price", jLabel26.getText());
                    parameters.put("payment", payment + ".00");
                    parameters.put("balance", jLabel30.getText());

                    TableModel tm = jTable1.getModel();
                    JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

                    JasperPrint jp = JasperFillManager.fillReport(c_jasp, parameters, dataSource);

                    JasperViewer.viewReport(jp, false);
                }

                resetFields();

                jButton1.setEnabled(true);
                jButton1.setText("Select Supplier");
                jLabel2.setText("none");
                jLabel4.setText("none");
                jLabel6.setText("none");
                jLabel8.setText("none");

                jLabel26.setText("0.00");
                jTextField3.setText("");
                jTextField3.setEditable(false);
                jComboBox1.setSelectedIndex(0);
                jLabel30.setText("0.00");

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

