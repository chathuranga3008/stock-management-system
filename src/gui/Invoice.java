    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String sid = jLabel10.getText();
        String pid = jLabel12.getText();
        String name = jLabel14.getText();
        String brand = jLabel16.getText();
        String series = jLabel20.getText();
        String category = jLabel18.getText();
        String qty = jTextField1.getText();
        String sellingPrice = jLabel22.getText();
        String unit = jLabel33.getText();
        String serialno = jTextField2.getText();
        String warranty = jTextField4.getText();

        if (sid.equals("none")) {
            JOptionPane.showMessageDialog(m, "Please select product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()) {
            JOptionPane.showMessageDialog(m, "Invalid quantity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(warranty).matches()) {
            JOptionPane.showMessageDialog(m, "Invalid warranty period", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `stock` WHERE `stock`.`id` = '" + sid + "'");
                rs.next();

                String availableQty = rs.getString("quantity");

                if (Integer.parseInt(availableQty) < Integer.parseInt(qty)) {
                    JOptionPane.showMessageDialog(m, "Quantity out of Stock", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                    boolean isFound = false;
                    int x = -1;

                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        String id = jTable1.getValueAt(i, 1).toString();

                        if (id.equals(sid)) {
                            isFound = true;
                            x = i;
                            break;
                        }
                    }

                    if (isFound) {
                        int option = JOptionPane.showConfirmDialog(m, "This product is already added. Do you want to update?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                        if (option == JOptionPane.YES_OPTION) {
                            int oldQty = Integer.parseInt(jTable1.getValueAt(x, 9).toString());
                            int finalQty = oldQty + Integer.parseInt(qty);

                            if (Integer.parseInt(availableQty) < (finalQty)) {
                                JOptionPane.showMessageDialog(m, "Quantity out of Stock", "Warning", JOptionPane.WARNING_MESSAGE);
                            } else {
                                jTable1.setValueAt(String.valueOf(finalQty), x, 9);

                                double updatedItemTotal = finalQty * Double.parseDouble(sellingPrice);
                                jTable1.setValueAt(String.valueOf(updatedItemTotal), x, 11);

                                updateTotal();
                            }
                        }

                        resetFields();

                    } else {
                        Vector v = new Vector();
                        v.add(category);
                        v.add(sid);
                        v.add(pid);
                        v.add(name);
                        v.add(brand);
                        v.add(series);
                        v.add(serialno);
                        v.add(warranty);
                        v.add(unit);
                        v.add(qty);
                        v.add(sellingPrice);

                        double itemtotal = Integer.parseInt(qty) * Double.parseDouble(sellingPrice);
                        v.add(df.format(itemtotal));

                        dtm.addRow(v);

                        updateTotal();
                        resetFields();
                        JOptionPane.showMessageDialog(m, "Product added to the Invoice", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(m, "Please select an Invoice item", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                int option = JOptionPane.showConfirmDialog(m, "Do you want to remove this item?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    dtm.removeRow(r);

                    updateTotal();

                    jLabel30.setText("0.00");
                    jTextField3.setText("");
                    jTextField3.setEditable(false);
                    jComboBox2.setSelectedIndex(0);
                    jLabel31.setText("0.00");

                    JOptionPane.showMessageDialog(m, "Invoice item removed", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

