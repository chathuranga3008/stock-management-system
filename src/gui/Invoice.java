    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String paymentType = jComboBox2.getSelectedItem().toString();
        String payment = jTextField3.getText();

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(m, "Please add products", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (paymentType.equals("Select")) {
            JOptionPane.showMessageDialog(m, "Please select payment method", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[0]|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(m, "Invalid payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            long mTime = System.currentTimeMillis();

            String uniqueId = mTime + "-" + Main.userId;

            String cid = jLabel2.getText();

            SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dNow = sdf1.format(new Date());

            MySQL.iud("INSERT INTO `invoice`(`customer_id`, `date_time`, `user_id`, `unique_id`) VALUES ('" + cid + "', '" + dNow + "', '" + Main.userId + "', '" + uniqueId + "')");

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `invoice` WHERE `unique_id`='" + uniqueId + "'");
                rs.next();
                String id = rs.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `payment_type` WHERE `name` = '" + paymentType + "'");
                rs2.next();
                String paymentTypeId = rs2.getString("id");

                String balance = jLabel31.getText();

                MySQL.iud("INSERT INTO `invoice_payment`(`invoice_id`, `payment_type_id`, `payment`, `balance`) VALUES ('" + id + "', '" + paymentTypeId + "', '" + payment + "', '" + balance + "')");

                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    String sid = jTable1.getValueAt(i, 1).toString();
                    String qty = jTable1.getValueAt(i, 9).toString();
                    String sno = jTable1.getValueAt(i, 6).toString();
                    String warranty = jTable1.getValueAt(i, 7).toString();

                    ResultSet rs3 = MySQL.search("SELECT * FROM `stock` WHERE `stock`.`id` = '" + sid + "'");
                    rs3.next();
                    String availableQty = rs3.getString("quantity");

                    int updatedQty = Integer.parseInt(availableQty) - Integer.parseInt(qty);

                    MySQL.iud("UPDATE `stock` SET `quantity` = '" + updatedQty + "' WHERE `id` = '" + sid + "'");

                    MySQL.iud("INSERT INTO `invoice_item`(`quantity`, `invoice_id`, `stock_id`) VALUES('" + qty + "', '" + id + "', '" + sid + "')");

                    ResultSet rs4 = MySQL.search("SELECT LAST_INSERT_ID()");
                    rs4.next();
                    String itemid = rs4.getString(1);

                    if (!sno.isBlank()) {
                        MySQL.iud("UPDATE `invoice_item` SET `serialno` = '" + sno + "' WHERE `id` = '" + itemid + "'");
                    }
                    if (!warranty.isBlank()) {
                        MySQL.iud("UPDATE `invoice_item` SET `warranty` = '" + warranty + "' WHERE `id` = '" + itemid + "'");
                    }
                }

                {
                    String filePath = "/reports/invoice.jrxml";

                    InputStream is = getClass().getResourceAsStream(filePath);

                    JasperReport c_jasp = (JasperReport) JasperCompileManager.compileReport(is);

                    HashMap parameters = new HashMap();
                    parameters.put("customer_name", jLabel4.getText());
                    parameters.put("customer_address", jLabel6.getText());
                    parameters.put("customer_contact", jLabel8.getText());
                    parameters.put("invoice_id", id);
                    parameters.put("invoice_date", dNow);
                    parameters.put("payment_mode", paymentType);
                    parameters.put("user_name", Main.userName);
                    parameters.put("total_price", jLabel30.getText());
                    parameters.put("payment", payment + ".00");
                    parameters.put("balance", jLabel31.getText());

                    TableModel tm = jTable1.getModel();
                    JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

                    JasperPrint jp = JasperFillManager.fillReport(c_jasp, parameters, dataSource);

                    JasperViewer.viewReport(jp, false);
                }

                resetFields();

                jButton1.setEnabled(true);
                jButton1.setText("Select Customer");
                jLabel2.setText("0");
                jLabel4.setText("none");
                jLabel6.setText("none");
                jLabel8.setText("none");

                jLabel30.setText("0.00");
                jTextField3.setText("");
                jTextField3.setEditable(false);
                jComboBox2.setSelectedIndex(0);
                jLabel31.setText("0.00");

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

