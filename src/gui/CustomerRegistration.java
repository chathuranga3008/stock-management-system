    private void loadCustomers() {
        try {
            ResultSet rs = MySQL.search("SELECT `customer`.`id`, `customer`.`fname`, `customer`.`lname`, `customer`.`contact`, `address`.`line_1`, `address`.`line_2`, `city`.`name` AS 'city' FROM `customer` INNER JOIN `address` ON `customer`.`address_id` = `address`.`id` INNER JOIN `city` ON `address`.`city_id` = `city`.`id` ORDER BY `id` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("line_1") + ", " + rs.getString("line_2"));
                v.add(rs.getString("city"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);
        } catch (Exception e) {
        }
    }

    private void loadCustomers(String text) {
        try {
            ResultSet rs = MySQL.search("SELECT `customer`.`id`, `customer`.`fname`, `customer`.`lname`, `customer`.`contact`, `address`.`line_1`, `address`.`line_2`, `city`.`name` AS 'city' FROM `customer` INNER JOIN `address` ON `customer`.`address_id` = `address`.`id` INNER JOIN `city` ON `address`.`city_id` = `city`.`id` WHERE `fname` LIKE '%" + text + "%' OR `lname` LIKE '%" + text + "%' OR `contact` LIKE '%" + text + "%' ORDER BY `id` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("line_1") + ", " + rs.getString("line_2"));
                v.add(rs.getString("city"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);
        } catch (Exception e) {
        }
    }

