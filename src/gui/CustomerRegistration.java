    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String contact = jTextField3.getText();
        String line_1 = jTextField4.getText();
        String line_2 = jTextField5.getText();
        String city = jComboBox1.getSelectedItem().toString();

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(m, "Enter First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(m, "Enter Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (contact.isEmpty()) {
            JOptionPane.showMessageDialog(m, "Enter Contact Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("0[7][01245678][0-9]{7}").matcher(contact).matches()) {
            JOptionPane.showMessageDialog(m, "Enter A Valid Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line_1.isEmpty()) {
            JOptionPane.showMessageDialog(m, "Enter Address Line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line_2.isEmpty()) {
            JOptionPane.showMessageDialog(m, "Enter Address Line 2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(m, "Enter City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT `id` FROM `city` WHERE `name` = '" + city + "'");
                rs1.next();
                String city_id = rs1.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `address` WHERE `line_1` = '" + line_1 + "' AND `line_2` = '" + line_2 + "' AND `city_id` = '" + city_id + "'");
                if (rs2.next()) {
                    JOptionPane.showMessageDialog(m, "Address already used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `address` (`line_1`, `line_2`, `city_id`) VALUES ('" + line_1 + "', '" + line_2 + "', '" + Integer.parseInt(city_id) + "')");

                    ResultSet rs3 = MySQL.search("SELECT LAST_INSERT_ID()");
                    rs3.next();
                    String address_id = rs3.getString(1);

                    MySQL.iud("INSERT INTO `customer` (`fname`, `lname`, `contact`, `address_id`) VALUES ('" + fname + "', '" + lname + "', '" + contact + "', '" + Integer.parseInt(address_id) + "')");

                    resetFields();
                    loadCustomers();

                    JOptionPane.showMessageDialog(m, "New customer adding complete", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

