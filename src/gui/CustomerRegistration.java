    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int r = jTable1.getSelectedRow();

        if (r == -1) {
            JOptionPane.showMessageDialog(m, "Please select a customer", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            if (evt.getClickCount() == 2) {
                if (this.i != null) {
                    String id = jTable1.getValueAt(r, 0).toString();
                    String name = jTable1.getValueAt(r, 1).toString().charAt(0) + ". " + jTable1.getValueAt(r, 2).toString();
                    String contact_number = jTable1.getValueAt(r, 3).toString();
                    String address = jTable1.getValueAt(r, 4).toString() + ", " + jTable1.getValueAt(r, 5).toString();

                    i.jLabel2.setText(id);
                    i.jLabel4.setText(name);
                    i.jLabel6.setText(address);
                    i.jLabel8.setText(contact_number);
                    m.backButton(this);
                } else if (this.rr != null) {
                    String id = jTable1.getValueAt(r, 0).toString();
                    String name = jTable1.getValueAt(r, 1).toString().charAt(0) + ". " + jTable1.getValueAt(r, 2).toString();
                    String contact_number = jTable1.getValueAt(r, 3).toString();

                    rr.jLabel9.setText(id);
                    rr.jLabel11.setText(name);
                    rr.jLabel15.setText(contact_number);
                    m.backButton(this);
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

